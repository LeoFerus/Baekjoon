import java.io.*;
import java.util.*;
import java.lang.*;


public class Main {
	public static void main(String[] args) throws IOException {
    Scanner sc = new Scanner(System.in);
    
    
    int r = sc.nextInt();
    
    int c = sc.nextInt();
    
    for(int i = 0; i<r; i++) {
    	
    	System.out.println("*".repeat(c));
    }
    
    
    
    
		  
	  
	}
}